--
-- Table structure for table `availability`
--
CREATE TABLE `availability` (
  `ID` int NOT NULL AUTO_INCREMENT,
  `FACILITY_BRANCH_ID` char(45) NOT NULL,
  `ACTIVITY_ID` int NOT NULL,
  `START_TIME` DATETIME NOT NULL,
  `END_TIME` DATETIME,
