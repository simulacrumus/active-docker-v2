--
-- Table structure for table `language`
--
CREATE TABLE `language` (
  `ID` char(2),
  `TITLE` varchar(45) NOT NULL,
  `LAST_UPDATED` DATETIME DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`ID`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

--
-- Table structure for table `translation`
--
CREATE TABLE `translation` (
  `ID` int NOT NULL,
  `LAST_UPDATED` DATETIME DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`ID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

--
-- Table structure for table `city`
--
CREATE TABLE `city` (
  `ID` int NOT NULL AUTO_INCREMENT,
  `TITLE` varchar(45) NOT NULL,
  `LAST_UPDATED` DATETIME DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`ID`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

--
-- Table structure for table `address`
--
CREATE TABLE `address` (
  `ID` int NOT NULL,
  `STREET_TRANSLATION_ID` int NOT NULL,
  `CITY` varchar(45) NOT NULL,
  `PROVINCE` varchar(45) NOT NULL,
  `POSTAL_CODE` char(6) NOT NULL,
  `COUNTRY` varchar(45) NOT NULL,
  `LONGITUDE` decimal(12,10) NOT NULL,
  `LATITUDE` decimal(12,10) NOT NULL,
  `LAST_UPDATED` DATETIME DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`ID`),
  KEY `STREET_TRANSLATION_ID` (`STREET_TRANSLATION_ID`),
  CONSTRAINT `ADDRESS_ibfk_1` FOREIGN KEY (`STREET_TRANSLATION_ID`) REFERENCES `translation` (`ID`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

--
-- Table structure for table `facility`
--
CREATE TABLE `facility` (
  `ID` int NOT NULL,
  `PHONE` varchar(12) NOT NULL,
  `EMAIL` varchar(50) NOT NULL,
  `TITLE_TRANSLATION_ID` int NOT NULL,
  `ADDRESS_ID` int NOT NULL,
  `URL_TRANSLATION_ID` int NOT NULL,
  `CITY_ID` int NOT NULL,
  `LAST_UPDATED` DATETIME DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`ID`),
  KEY `TITLE_TRANSLATION_ID` (`TITLE_TRANSLATION_ID`),
  KEY `ADDRESS_ID` (`ADDRESS_ID`),
  KEY `URL_TRANSLATION_ID` (`URL_TRANSLATION_ID`),
  KEY `CITY_ID` (`CITY_ID`),
  CONSTRAINT `FACILITY_ibfk_1` FOREIGN KEY (`TITLE_TRANSLATION_ID`) REFERENCES `translation` (`ID`),
  CONSTRAINT `FACILITY_ibfk_2` FOREIGN KEY (`ADDRESS_ID`) REFERENCES `address` (`ID`),
  CONSTRAINT `FACILITY_ibfk_3` FOREIGN KEY (`URL_TRANSLATION_ID`) REFERENCES `translation` (`ID`),
  CONSTRAINT `FACILITY_ibfk_4` FOREIGN KEY (`CITY_ID`) REFERENCES `city` (`ID`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

--
-- Table structure for table `category`
--
CREATE TABLE `category` (
  `ID` int NOT NULL,
  `TITLE_TRANSLATION_ID` int NOT NULL,
